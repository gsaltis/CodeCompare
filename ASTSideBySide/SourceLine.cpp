SourceLine::GetSourceFileName(void)
{
  return SourceFileName;
}

/*****************************************************************************!
 * Function : GetFullSourceFileName
 *****************************************************************************/
QString
SourceLine::GetFullSourceFileName(void)
{
  return SourceFullFileName;
}
